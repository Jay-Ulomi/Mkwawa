            <h2>Organizational Structure</h2>
            <p>
                From the beginning, Tanzanian tobacco farmers understood the strength in numbers and formed cooperative societies. These cooperatives, structured around regional administrative boundaries, served as the foundation for marketing crops and securing resources.
            </p>
            <ul>
                <li><strong>Typical Cooperative:</strong> 200-400 members from one or more nearby villages.</li>
                <li><strong>Current Statistics:</strong> Approximately thousands of registered primary societies exist today in Tanzania, with more farmers growing tobacco independently.</li>
            </ul>
        </div>
    </div>

    <div class="row gy-1 align-items-center features-item">
        <div class="col order-2 order-lg-1" data-aos="fade-up" data-aos-delay="200">
            <h2>Governance and Regulation</h2>
            <p>
                The tobacco industry in Tanzania is regulated by the Tanzania Tobacco Board (TTB), ensuring that all tobacco is grown through a structured tripartite contract farming system involving primary societies, financiers, and leaf merchants.
            </p>
            <ul>
                <li><strong>Key Regulatory Body:</strong> Tanzania Tobacco Board (TTB)</li>
                <li><strong>Contract Farming:</strong> Involves primary societies, financiers, and leaf merchants.</li>
                <li><strong>Input Provision:</strong> Managed by the Tanzania Tobacco Council (TTC), which sets the types and prices of crop inputs.</li>
            </ul>
        </div>
    </div>

    <div class="row gy-1 align-items-center features-item">
        <div class="col order-2 order-lg-1" data-aos="fade-up" data-aos-delay="200">
            <h2>Financial Support and Sustainability</h2>
            <p>
                Primary societies acquire loans from financiers to purchase approved inputs, with these loans repaid through tobacco sales. This system guarantees that farmers have the necessary resources while maintaining financial accountability.
            </p>
        </div>
    </div>

