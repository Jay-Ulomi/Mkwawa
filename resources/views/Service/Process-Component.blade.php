            <h3>2. Curing</h3>
            <p>Curing is essential for drying harvested tobacco leaves to prepare them for further processing.</p>
            <ul>
              <li><strong>Air-Curing:</strong> Produces low sugar, high nicotine tobacco, ideal for cigars.</li>
              <li><strong>Flue-Curing:</strong> Creates higher sugar content tobacco, typically used in cigarettes.</li>
              <li><strong>Sun-Curing:</strong> Naturally dries leaves under the sun, resulting in low sugar, low nicotine tobacco.</li>
            </ul>
